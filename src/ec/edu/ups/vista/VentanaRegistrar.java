    public boolean comprobarCampos() {
        if (txtNombre.getText().isBlank() || txtApellido.getText().isBlank() || txtCedula.getText().isBlank() || txtDireccion.getText().isBlank() || txtDia.getText().isBlank() || txtMes.getText().isBlank() || txtAño.getText().isBlank() || txtCorreo.getText().isBlank() || txtContraseña.getText().isBlank()) {
            JOptionPane.showMessageDialog(this, "Faltan Campos por llenar");
            return true;
        }
        return false;
    }

