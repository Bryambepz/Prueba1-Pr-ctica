    private void btnTestigosActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTestigosActionPerformed
        // TODO add your handling code here:
        int dia = Integer.valueOf(txtDia.getText());
        int mes = Integer.valueOf(txtMes.getText());
        int anio = Integer.valueOf(txtAño.getText());
        Date fecha = new Date(anio-1900, mes, dia);
        
        if (!txtNombre.getText().isEmpty() || !txtApellido.getText().isEmpty() || !txtCedula.getText().isEmpty()
                || !txtDireccion.getText().isEmpty() || !txtGenero.getText().isEmpty() || !txtDia.getText().isEmpty() 
                || !txtMes.getText().isEmpty() || !txtAño.getText().isEmpty() || !txtCorreo.getText().isEmpty() || !txtContraseña.getText().isEmpty()) {
            if (ctrlUsuario.comprobarCorreo(txtCorreo.getText())) {
                contador++;
                var us = new Usuario(txtNombre.getText(), txtApellido.getText(), txtCedula.getText(), txtDireccion.getText(), txtGenero.getText(), fecha, txtCorreo.getText(), txtContraseña.getText(), "Testigos") {};
                ctrlUsuario.create(us);
                System.out.println("new us = " + us);
                JOptionPane.showMessageDialog(this, "Registrado correctamente");
                if (contador == 2) {
                    btnTestigos.setEnabled(false);
                    contador=0;
                    vaciar();
                }else{
                    JOptionPane.showMessageDialog(this, "Ingrese siguiente testigo");
                    vaciar();
                }
            }else{
                JOptionPane.showMessageDialog(this, "Correo ya usado");
            }
        }else{
            JOptionPane.showMessageDialog(this, "Hay campos en blanco");
        }
    }//GEN-LAST:event_btnTestigosActionPerformed

